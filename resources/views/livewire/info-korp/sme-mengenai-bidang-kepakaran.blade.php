        <p class="mb-0">
            Inisiatif Kemajuan Kerjaya Laluan Pakar Bidang Khusus (Subject Matter Expert - SME) yang diperkenalkan oleh
            Kerajaan adalah sejajar dengan Rancangan Malaysia Kesebelas (RMKe11) mengenai pengukuhan pengurusan bakat
            untuk
            perkhidmatan awam masa hadapan. Di samping itu, Dokumen Kajian Separuh Penggal RMKe11 menggariskan kemajuan
            kerjaya pegawai perkhidmatan awam akan dilaksanakan dengan lebih telus berdasarkan merit dan prestasi dalam
            perkhidmatan.
        </p>
        <div class="para-gap"></div>
        <p class="mb-0 para-align">
            Melalui pewujudan laluan SME Kerajaan dapat mengiktiraf pegawai yang memiliki keupayaan intelektual,
            kepakaran
            yang tinggi serta berpengalaman luas dalam bidang kepakaran serta menjadi sumber rujukan dalam dan luar
            organisasinya ke arah memastikan pengekalan bakat terbaik dan berkepakaran tinggi dalam perkhidmatan awam.
        </p>
        <div class="para-gap"></div>
        <p class="mb-0 para-align">
            Proses pengiktirafan pegawai SME dalam perkhidmatan awam bermula daripada fasa perancangan iaitu penentuan
            bidang kepakaran, penetapan kriteria penilaian, unjuran keperluan dan seterusnya perakuan ke Lembaga
            Kenaikan
            Pangkat Perkhidmatan Awam (LKPPA) untuk kelulusan bidang dan kriteria penilaian. Permohonan ke laluan SME
            boleh
            dikemukakan setelah bidang kepakaran diluluskan dan dipersetujui oleh LKPPA. Pegawai yang diiktiraf sebagai
            pegawai SME oleh Lembaga Kenaikan Pangkat Perkhidmatan Awam (LKPPA) akan diberi pemangkuan dan seterusnya
            kenaikan pangkat pada 1 gred yang lebih tinggi secara Khas Untuk Penyandang (KUP) tertakluk melepasi tapisan
            kepakaran yang ketat. Pegawai SME perlu melaksanakan Penugasan Khas Pakar Bidang Khusus (SME Special
            Assignment
            - SSA) di samping tugasan hakiki berdasarkan jawatan bagi tujuan pengekalan dan peningkatan di laluan SME.
        </p>
        <div class="para-gap"></div>
        <p class="mb-0 para-align">
            Dengan wujudnya pegawai SME yang berterusan menyumbangkan kepakaran dalam perkhidmatan awam, negara akan
            memperoleh manfaat jangka panjang melalui sumbangan kepakaran dalam perkhidmatan awam di samping memastikan
            berlakunya pemindahan kepakaran secara sistematik dan efektif dalam kalangan penjawat awam di bawah pelan
            penggantian dan pembangunan bakat perkhidmatan awam. Secara tidak langsung kebergantungan kepakaran daripada
            pihak luar dapat dikurangkan dan penjimatan kos dapat dihasilkan selaras dengan falsafah SME.
        </p>
